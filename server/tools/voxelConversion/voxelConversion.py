SLICE_COUNT = 16
BOARD_WIDTH = 16
BOARD_HEIGHT = 32

HEADER_OUTPUT_FILENAME = "voxelData.h"

#globals
##########################################################
args = None
parser = None

def plotVoxels(voxels):
    '''
    Plot converted voxels using mat plot lib 3D projection plot
    
    :param voxels: List of voxels (x, y, z)
    '''
    xList, yList, zList = zip(*voxels)#unpack coords for matplot lib
    ##setup minimum matplotlib 3D plot
    fig = plt.figure()
    ax = fig.add_subplot(111, projection="3d")
    ax.scatter(xList, yList, zList, marker='s', s=20)
    plt.show()

#arg actions
##########################################################
def actionDebugVisualize():
    '''
    visualize .obj in matplotlib as 3d plot
    '''
    objFilepath = argsGetFilepath()
    voxels = getVoxelsFromObj(objFilepath)#get the voxels from file
    print(f"debug {args.debug}")
    plotVoxels(voxels)#visualize the voxels for debug

def actionConvertToHeader():
    '''
    convert .obj file to a C header in quantized cylindrical coords
    '''
    
    global SLICE_COUNT, BOARD_WIDTH, BOARD_HEIGHT
    objFilepath = argsGetFilepath()
    #load the obj
    cartesianVoxels = getVoxelsFromObj(objFilepath)
    cylindricalVoxels = cartesianList2Cylindrical(cartesianVoxels)

    quantizedVoxels = cylindricalList2Quantized(cylindricalVoxels, SLICE_COUNT, BOARD_WIDTH, BOARD_HEIGHT)
    voxels = [[[0 for _ in range(BOARD_WIDTH)] for _ in range(BOARD_HEIGHT)]for _ in range(SLICE_COUNT)]
    
    for sliceN, h, r in quantizedVoxels:#build the bit array
        voxels[sliceN][h][r] = 1
    #turn our 3d LIST into a 2d list
    flat = []
    for sliceN in range(SLICE_COUNT):
        for h in range(BOARD_HEIGHT):
            for r in range(BOARD_WIDTH):
                flat.append(voxels[sliceN][h][r])
    #build the output string
    dataStringLines = []
    valuesPerLine = 16#change the to change how long each data segment is in the data formatting
    for i in range(0, len(flat), valuesPerLine):
        chunk = flat[i:i + valuesPerLine]
        line = "    " + ", ".join(str(v) for v in chunk)
        if i + valuesPerLine < len(flat):
            line += ","
        dataStringLines.append(line)
    dataString = "\n".join(dataStringLines)
    outputString = f'''
// Data auto generated by 3D hologram VoxelConversion tool
//  model source file: {objFilepath}
#define MVOX_VOXEL_SLICES {SLICE_COUNT}
#define MVOX_VOXEL_HEIGHT {BOARD_HEIGHT}
#define MVOX_VOXEL_WIDTH {BOARD_WIDTH}
#define MVOX_VOXEL_COUNT {len(flat)}

const uint8_t voxelData[MVOX_VOXEL_COUNT] = {{
{dataString}
}};
'''
    f = open(HEADER_OUTPUT_FILENAME, "w")
    f.write(outputString)


# arg functions
##########################################################
def argsInit():
    '''
    Initialize parser and args. Parse all given args.
    '''
    global args, parser
    parser = argparse.ArgumentParser(description=HELPSTRING, formatter_class=argparse.RawTextHelpFormatter)
    parser.add_argument("objFilepath", nargs="?", help="Optional .obj file path")
    parser.add_argument("-d", "--debug", action="store_true")
    parser.add_argument("-dv", "--debugVisualize", action="store_true",  help="visualize .obj in matplotlib as 3d plot")
    parser.add_argument("-ch", "--convertHeader", action="store_true", help="convert .obj file to a C header in quantized cylindrical coords")
    args = parser.parse_args()

def argsParseAndRunFlags():
    '''
    Look through all the given flag arguements and run what is needed based on what is given
    '''
    global args, parser
    if args.debugVisualize:
        actionDebugVisualize()
    if args.convertHeader:
        actionConvertToHeader()


def argsGetFilepath():
    '''
    get the filepath arg from args
    '''
    global args, parser
    objFilepath = DEFAULT_OBJ_FILEPATH
    if args.objFilepath:#set custom filepath if given
        givenPath = args.objFilepath
        if not os.path.exists(givenPath):#does the file exist
            parser.error(f"file {givenPath} not found")
        objFilepath = givenPath
    return objFilepath

