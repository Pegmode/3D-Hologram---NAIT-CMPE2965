def actionConvertToHeader():
    '''
    convert .obj file to a C header in quantized cylindrical coords
    '''
    global SLICE_COUNT, BOARD_WIDTH, BOARD_HEIGHT

    packedBytes, objFilepath, quantizedVoxels, flatBitCount, _  = convertFullToPackedBytes()
    #build the output string
    dataStringLines = []
    valuesPerLine = 16#change the to change how long each data segment is in the data formatting
    for i in range(0, len(packedBytes), valuesPerLine):
        chunk = packedBytes[i:i + valuesPerLine]
        line = "    " + ", ".join(f"0x{b:02X}" for b in chunk)
        if i + valuesPerLine < len(packedBytes):
            line += ","
        dataStringLines.append(line)
    dataString = "\n".join(dataStringLines)
    outputString = f'''
// Data auto generated by 3D hologram VoxelConversion tool
//  model source file: {objFilepath}
#define MVOX_VOXEL_SLICES {SLICE_COUNT}
#define MVOX_VOXEL_HEIGHT {BOARD_HEIGHT}
#define MVOX_VOXEL_WIDTH {BOARD_WIDTH}
#define MVOX_VOXEL_COUNT {len(quantizedVoxels)}//The number of bits = 1
#define MVOX_VOXEL_BIT_COUNT {flatBitCount}
#define MVOX_VOXEL_BYTE_COUNT {len(packedBytes)}

const uint8_t debugMvox_voxelData[MVOX_VOXEL_BYTE_COUNT] = {{
{dataString}
}};
'''
    f = open(HEADER_OUTPUT_FILENAME, "w")
    f.write(outputString)


def actionConvertToPipe():
    '''
    convert an .obj file to custom byte array and return in a windows pipe
    '''
    global args, parser, SLICE_COUNT, BOARD_WIDTH, BOARD_HEIGHT
    packedBytes, objFilepath, _, _, _ = convertFullToPackedBytes()
    #Send the frame over pipe
    print("Writing voxel data to pipe...")
    sys.stdout.flush()
    pipeName = r'\\.\pipe\VoxelPipe'
    try:
        handle = win32file.CreateFile(
            pipeName,
            win32file.GENERIC_WRITE,
            0, None,
            win32file.OPEN_EXISTING,
            0, None
        )    
    except:
        print(f"[ERROR] named pipe {pipeName} not found!! (has the UI process created the pipe yet? )")
        sys.stdout.flush()
        return
    data = bytearray(packedBytes)
    win32file.WriteFile(handle, data)
    print(f"finished converting {objFilepath}!")
    sys.stdout.flush()

def actionConvertToAnimatedPipe():
    '''
    convert an .obj file to custom byte array animation and return in a windows pipe
    
    writes a header of the format
        struct{
            char[] header = {'V','O','X'},
            UINT64 pipeMessageSize = size of header + data in bytes// little endian
            UINT32 frameCount = frameCount//little endian
        }
    
    followed by the voxel data
    '''
    global args, parser, SLICE_COUNT, BOARD_WIDTH, BOARD_HEIGHT
    ##Convert everything...
    objFilepath = argsGetFilepath()
    #load the obj and convert the coords to quantized cylindrical
    print(f"converting {objFilepath} to cartesian voxels...")
    sys.stdout.flush()
    cartesianVoxels = getVoxelsFromObj(objFilepath, args.fillVoxels)
    print(f"converting cartesian to cylindrical...")
    sys.stdout.flush()
    cylindricalVoxels = cartesianList2Cylindrical(cartesianVoxels)
    print(f"quantizing cylindrical coordinates...")
    sys.stdout.flush()
    quantizedVoxels = cylindricalList2Quantized(cylindricalVoxels, SLICE_COUNT, BOARD_WIDTH, BOARD_HEIGHT)
    ## Simple vertical animation...
    print(f"Performing vertical transforms for simple animation")
    sys.stdout.flush()
    maxHeight = max(h for _, h, _ in quantizedVoxels)
    maxOffset = (BOARD_HEIGHT - 1) - maxHeight
    if maxOffset < 0:
        print("Model already exceeds board height!")
        sys.stdout.flush()
        return
    offsets = list(range(0, maxOffset + 1)) + list(range(maxOffset - 1, -1 ,-1))
    offsets = [x for x in offsets for _ in range(bounceMultiplier)]# repeat the frame N times to control speed...
    animatedFrames = []
    animatedFrames.extend(packFlattenedVoxelsToBytes(convertVoxelsToFlatList(quantizedVoxels, SLICE_COUNT, BOARD_WIDTH, BOARD_HEIGHT)))
    frameCount = 1
    frameSize = len(quantizedVoxels)
    for currentOffset in offsets:#perform all the transformations... pack the data here to not overwhelm the converter
        currentFrame = [(sliceN, h + currentOffset, r) for sliceN, h, r in quantizedVoxels]
        currentFrameFlatList = convertVoxelsToFlatList(currentFrame, SLICE_COUNT, BOARD_WIDTH, BOARD_HEIGHT)
        currentFramePacked = packFlattenedVoxelsToBytes(currentFrameFlatList)
        animatedFrames.extend(currentFramePacked)
        frameCount += 1
    #Send the data to the pipe
    data = bytearray(animatedFrames)
    print(f"writing pipe header w/ frameCount:{frameCount}")
    sys.stdout.flush()
    headerSize = 3 + 8 + 4#need size of header before the header is made... chicken and the egg... write this manually ):
    headerStruct = (b'VOX'  + #header data....
                    struct.pack('<Q', headerSize + len(data)) + 
                    struct.pack('<I', frameCount)
    )
    pipeName = r'\\.\pipe\VoxelPipe' #open the pipe...
    try:
        handle = win32file.CreateFile(
            pipeName,
            win32file.GENERIC_WRITE,
            0, None,
            win32file.OPEN_EXISTING,
            0, None
        )    
    except:
        print(f"[ERROR] named pipe {pipeName} not found!! (has the UI process created the pipe yet? )")
        sys.stdout.flush()
        return

    #Send the frame over pipe
    print("Writing voxel data to pipe...")
    sys.stdout.flush()
    win32file.WriteFile(handle, headerStruct + data)
    print(f"finished converting {objFilepath}!")
    sys.stdout.flush()

def actionDebugCylindrical():
    '''
    Output a test polar graph at the cylindrical pipeline step
    
    write the output to a .gif in script directory called "CylindricalAnimation.gif"
    '''
    global BOARD_HEIGHT
    plotImages = []
    _, _, _, _, cylindricalVoxels  = convertFullToPackedBytes()

    i = 0
    for currentH in range(BOARD_HEIGHT):
        filteredVoxels = [(theta, r) for theta, r, h in cylindricalVoxels if h == currentH]
        if len(filteredVoxels) == 0:
            theta , r = [],[]
        else:
            theta, r = zip(*filteredVoxels)
        fig, ax = plt.subplots(subplot_kw={'projection' : 'polar'})
        ax.scatter(theta, r, color="blue", linewidth=2)
        plt.title(f"horizontal polar slice")
        #plt.show()
        fig.canvas.draw()
        buffer = BytesIO()
        plt.savefig(buffer, format="png")
        buffer.seek(0)
        image = Image.open(buffer).convert("RGB")
        image.load()
        draw = ImageDraw.Draw(image)
        #text fields
        text = f"""
H =  {currentH}

"""
        draw.multiline_text((0,0), text, fill = "black")
        plotImages.append(image)
        buffer.close()
        plt.close()
        i += 1
    plotImages[0].save("CylindricalAnimation.gif", save_all = True, append_images = plotImages[1:], duration = 500, loop = 0)

    


