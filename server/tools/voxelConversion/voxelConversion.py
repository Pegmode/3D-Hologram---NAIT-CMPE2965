def argsInit():
    '''
    Initialize parser and args. Parse all given args.
    '''
    global args, parser, SLICE_COUNT
    parser = argparse.ArgumentParser(description=HELPSTRING, formatter_class=argparse.RawTextHelpFormatter)
    parser.add_argument("objFilepath", nargs="?", help="Optional .obj file path")
    parser.add_argument("-d", "--debug", action="store_true")
    parser.add_argument("-dv", "--debugVisualize", action="store_true",  help="visualize .obj in matplotlib as 3d plot")
    parser.add_argument("-ch", "--convertHeader", action="store_true", help="convert .obj file to a C header in quantized cylindrical coords")
    parser.add_argument("-cp", "--convertPipe", action="store_true", help="convert .obj file to a byte array returned in a windows pipe. The UI pipe server MUST be running with named pipe VoxelPipe")
    parser.add_argument("-sc", "--sliceCount", type=int, help=f"override the default slicecount, default:{SLICE_COUNT}")
    parser.add_argument("-cpa","--convertPipeAnimated", action="store_true", help="convert .obj file to a byte array, formatted as an animation returned in a windows pipe. The UI pipe server MUST be running with named pipe VoxelPipe")
    parser.add_argument("-as", "--animationSpeed", type=int, help=f"Set the animation speed, default:{bounceMultiplier}")
    parser.add_argument("-fv", "--fillVoxels", action="store_true", help="Enable fill when converting voxels (takes a longer time)")
    parser.add_argument("-dcy", "--debugCylindrical", action="store_true", help="visualize slices of the cylindrical step")
    args = parser.parse_args()

def argsParseAndRunFlags():
    '''
    Look through all the given flag arguements and run what is needed based on what is given
    '''
    global args, parser, bounceMultiplier, SLICE_COUNT
    #value args
    if args.sliceCount:#override the default slice count if arg is given.
        SLICE_COUNT = args.sliceCount
    if args.animationSpeed:
        bounceMultiplier = args.animationSpeed
    
    #actions
    if args.debugVisualize:
        actionDebugVisualize()
    if args.convertHeader:
        actionConvertToHeader()
    if args.convertPipe:
        actionConvertToPipe()
    if args.convertPipeAnimated:
        actionConvertToAnimatedPipe()
    if args.debugCylindrical:
        actionDebugCylindrical()


def argsGetFilepath():
    '''
    get the filepath arg from args
    '''
    global args, parser
    objFilepath = DEFAULT_OBJ_FILEPATH
    if args.objFilepath:#set custom filepath if given
        givenPath = args.objFilepath
        if not os.path.exists(givenPath):#does the file exist
            parser.error(f"file {givenPath} not found")
            sys.stdout.flush()
        objFilepath = givenPath
    return objFilepath

